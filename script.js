const menuButton = document.querySelector(".menu-button");
const mainNav = document.querySelector("#mainNav");
const searchInput = document.querySelector("#quizSearch");
const monthGroups = [...document.querySelectorAll(".month-group")];
const quizTabs = document.querySelector("#quizTabs");
const quizForm = document.querySelector("#quizForm");
const submitQuiz = document.querySelector("#submitQuiz");
const resetQuiz = document.querySelector("#resetQuiz");
const quizScore = document.querySelector("#quizScore");
const activeSource = document.querySelector("#activeSource");
let activeQuiz = quizData[0];

menuButton.addEventListener("click", () => {
  const nextState = menuButton.getAttribute("aria-expanded") !== "true";
  menuButton.setAttribute("aria-expanded", String(nextState));
  mainNav.classList.toggle("is-open", nextState);
});

searchInput.addEventListener("input", () => {
  const query = searchInput.value.trim().toLowerCase();

  monthGroups.forEach((group) => {
    const rows = [...group.querySelectorAll("li")];
    let visibleRows = 0;

    rows.forEach((row) => {
      const matches = row.textContent.toLowerCase().includes(query) || group.dataset.month.toLowerCase().includes(query);
      row.hidden = query.length > 0 && !matches;
      if (!row.hidden) visibleRows += 1;
    });

    group.hidden = visibleRows === 0;
  });
});

function renderTabs() {
  quizTabs.innerHTML = quizData.map((quiz) => `
    <button type="button" role="tab" data-quiz-id="${quiz.id}" aria-selected="${quiz.id === activeQuiz.id}">
      ${quiz.date}
    </button>
  `).join("");
}

function renderQuiz() {
  activeSource.href = activeQuiz.source;
  quizScore.value = "";
  quizForm.innerHTML = activeQuiz.questions.map((question, index) => `
    <fieldset class="question-block" data-question-index="${index}">
      <legend><span>${index + 1}</span>${question.prompt}</legend>
      <div class="options">
        ${question.options.map((option, optionIndex) => `
          <label>
            <input type="radio" name="question-${index}" value="${optionIndex}">
            <span>${option}</span>
          </label>
        `).join("")}
      </div>
      <p class="explanation" hidden>${question.explanation}</p>
    </fieldset>
  `).join("");
}

function setActiveQuiz(quizId) {
  const nextQuiz = quizData.find((quiz) => quiz.id === quizId);
  if (!nextQuiz) return;
  activeQuiz = nextQuiz;
  renderTabs();
  renderQuiz();
}

function gradeQuiz() {
  let correct = 0;

  activeQuiz.questions.forEach((question, index) => {
    const fieldset = quizForm.querySelector(`[data-question-index="${index}"]`);
    const selected = quizForm.querySelector(`input[name="question-${index}"]:checked`);
    const selectedAnswer = selected ? Number(selected.value) : -1;
    const isCorrect = selectedAnswer === question.answer;

    fieldset.classList.toggle("is-correct", isCorrect);
    fieldset.classList.toggle("is-incorrect", !isCorrect);
    fieldset.querySelector(".explanation").hidden = false;
    if (isCorrect) correct += 1;
  });

  quizScore.value = `Score: ${correct}/${activeQuiz.questions.length}`;
}

quizTabs.addEventListener("click", (event) => {
  const button = event.target.closest("[data-quiz-id]");
  if (!button) return;
  setActiveQuiz(button.dataset.quizId);
});

document.querySelectorAll("[data-quiz-id]").forEach((link) => {
  link.addEventListener("click", () => setActiveQuiz(link.dataset.quizId));
});

submitQuiz.addEventListener("click", gradeQuiz);
resetQuiz.addEventListener("click", renderQuiz);

renderTabs();
renderQuiz();
