const menuButton = document.querySelector(".menu-button");
const mainNav = document.querySelector("#mainNav");
const pibDateSelect = document.querySelector("#pibDateSelect");
const loadPibDate = document.querySelector("#loadPibDate");
const dailyPibIntro = document.querySelector("#dailyPibIntro");
const summaryDate = document.querySelector("#summaryDate");
const summaryTitle = document.querySelector("#summaryTitle");
const summaryLead = document.querySelector("#summaryLead");
const summaryBlocks = document.querySelector("#summaryBlocks");
const prelimsTriggers = document.querySelector("#prelimsTriggers");
const mainsAngles = document.querySelector("#mainsAngles");
const summarySources = document.querySelector("#summarySources");

function today() {
  return new Date().toISOString().slice(0, 10);
}

function escapeHtml(value) {
  return value.replace(/[&<>"']/g, (char) => ({
    "&": "&amp;",
    "<": "&lt;",
    ">": "&gt;",
    '"': "&quot;",
    "'": "&#039;"
  }[char]));
}

function escapeAttribute(value) {
  return escapeHtml(value).replace(/`/g, "&#096;");
}

function renderList(target, items) {
  target.innerHTML = items.map((item) => `<li>${escapeHtml(item)}</li>`).join("");
}

function renderDailySummary(summary) {
  dailyPibIntro.textContent = summary.date === today()
    ? "Showing today's PIB one-pager. Switch the date to revise earlier briefs."
    : "Showing saved PIB one-pager for the selected date.";
  summaryDate.dateTime = summary.date;
  summaryDate.textContent = summary.label;
  summaryTitle.textContent = summary.title;
  summaryLead.textContent = summary.lead;
  summaryBlocks.innerHTML = summary.blocks.map((block) => `
    <section class="summary-block">
      <h4>${escapeHtml(block.heading)}</h4>
      <ul>
        ${block.points.map((point) => `<li>${escapeHtml(point)}</li>`).join("")}
      </ul>
    </section>
  `).join("");
  renderList(prelimsTriggers, summary.prelims);
  renderList(mainsAngles, summary.mains);
  summarySources.innerHTML = summary.sources.map((source) => `
    <a href="${escapeAttribute(source.url)}" target="_blank" rel="noreferrer">
      ${escapeHtml(source.label)}
      <small>${escapeHtml(source.note)}</small>
    </a>
  `).join("");
}

function setupDailySummaries() {
  if (pibDateSelect.options.length === 0) {
    pibDateSelect.innerHTML = dailySummaries.map((summary) => `
      <option value="${summary.date}">${summary.label}</option>
    `).join("");
  }

  const currentSummary = dailySummaries.find((summary) => summary.date === today()) || dailySummaries[0];
  pibDateSelect.value = currentSummary.date;
  renderDailySummary(currentSummary);
}

function loadSelectedSummary() {
  const summary = dailySummaries.find((item) => item.date === pibDateSelect.value);
  if (summary) renderDailySummary(summary);
}

menuButton.addEventListener("click", () => {
  const nextState = menuButton.getAttribute("aria-expanded") !== "true";
  menuButton.setAttribute("aria-expanded", String(nextState));
  mainNav.classList.toggle("is-open", nextState);
});

pibDateSelect.addEventListener("change", loadSelectedSummary);
loadPibDate.addEventListener("click", loadSelectedSummary);

setupDailySummaries();
